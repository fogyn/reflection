public class ParentC {

    private String nameC;
    private int ageC;

    public ParentC() {
    }

    public ParentC(String nameC, int ageC) {
        this.nameC = nameC;
        this.ageC = ageC;
    }

    public String getNameC() {
        return nameC;
    }

    public void setNameC(String nameC) {
        this.nameC = nameC;
    }

    public int getAgeC() {
        return ageC;
    }

    public void setAgeC(int ageC) {
        this.ageC = ageC;
    }
}
